hours = 24

while hours > 0:
    print(f"You have {hours} hour(s) left.")
    hours -= 1
print("Your time is up!")

# While loops are used to repeat a block of code as long as a condition is true. 
# In this example, the loop continues to run as long as the variable 'hours' is greater than 0. 
# Each iteration of the loop prints the number of hours left and then decrements the 'hours' variable by 1. 
# When 'hours' reaches 0, the loop ends and the final message "Your time is up!" is printed.