fruits = ["Apple", "Peach", "Pear"]
# This is a list of fruits that we will loop through.
for fruit in fruits:
    # This is a for loop that iterates through each fruit in the list of fruits.
    print(fruit)
    # Prints the name of the fruit.
    print(fruit + " pie")
    # Prints the name of the fruit followed by " pie".
print(fruits)
# Prints the list of fruits after the loop is done.
