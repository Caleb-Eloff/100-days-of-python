# Printing is a function that allows us to display information
# on the screen. In this case, we are printing the string
# "Hello world!" to the console. The text inside the parentheses
# and quotes is called a string literal, and it will be
# displayed exactly as it is written.
print("Hello world!")

# We can also print multiple lines of text by using the newline
# character, which is represented by \n. This tells the program
# to start a new line after the first string is printed.
print("Hello world!\nHello world!")

# We can also concatenate strings using the + operator. This allows
# us to combine multiple strings into one. In this case, we are
# concatenating the strings "Hello", " ", and "world!".
print("Hello" + " " + "world!")